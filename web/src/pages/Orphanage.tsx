import React, { useEffect, useState } from "react";
import { FaWhatsapp } from "react-icons/fa";
import { FiClock, FiInfo } from "react-icons/fi";
import { Map, Marker, TileLayer } from "react-leaflet";

import "../styles/pages/orphanage.css";
import SideBar from "../components/SideBar";
import { mapIcon } from "../utils/mapIcon";
import api from "../services/api";
import { useParams } from "react-router-dom";

// Props for the Orphanage
interface OrphanageProps {
  id: number;
  latitude: number;
  longitude: number;
  name: string;
  description: string;
  instructions: string;
  opening_hours: string;
  open_on_weekends: string;
  phone: string;
  images: Array<{
    id: number;
    url: string;
  }>;
}
// Props for the URL Params
interface OrphanageParams {
  id: string;
}

export default function Orphanage() {
  // Initial Data
  const [orphanage, setOrphanage] = useState<OrphanageProps>();
  const params = useParams<OrphanageParams>();
  const [activeImageIndex, setActiveImageIndex] = useState(0);

  // Connection with the database to get all the data from specified ID
  useEffect(() => {
    api.get(`orphanages/${params.id}`).then((res) => {
      setOrphanage(res.data);
    });
  }, [params.id]);

  // Simple loading message
  if (!orphanage) {
    return <p>Carregando</p>;
  }

  return (
    <div id="page-orphanage">
      <SideBar />
      <main>
        <div className="orphanage-details">
          <img
            src={orphanage.images[activeImageIndex].url}
            alt={orphanage.name}
          />

          <div className="images">
            {orphanage.images.map((image, index) => {
              return (
                <button
                  onClick={() => {
                    setActiveImageIndex(index);
                  }}
                  key={image.id}
                  className={activeImageIndex === index ? "active" : ""}
                  type="button"
                >
                  <img src={image.url} key={image.id} alt={orphanage.name} />
                </button>
              );
            })}
          </div>

          <div className="orphanage-details-content">
            <h1>{orphanage.name}</h1>
            <p>{orphanage.description}</p>

            <div className="map-container">
              <Map
                center={[orphanage.latitude, orphanage.longitude]}
                zoom={16}
                style={{ width: "100%", height: 280 }}
                dragging={false}
                touchZoom={false}
                zoomControl={false}
                scrollWheelZoom={false}
                doubleClickZoom={false}
              >
                <TileLayer url="https://a.tile.openstreetmap.org/{z}/{x}/{y}.png" />
                <Marker
                  interactive={false}
                  icon={mapIcon}
                  position={[orphanage.latitude, orphanage.longitude]}
                />
              </Map>

              <footer>
                <a
                  target="_blank"
                  rel="noopener noreferrer"
                  href={`http://www.google.com/maps/dir/?api=1&destination=${orphanage.latitude},${orphanage.longitude}`}
                >
                  Ver rotas no Google Maps
                </a>
              </footer>
            </div>

            <hr />

            <h2>Instruções para visita</h2>
            <p>{orphanage.instructions}</p>

            <div className="open-details">
              <div className="hour">
                <FiClock size={32} color="#15B6D6" />
                Segunda à Sexta <br />
                {orphanage.opening_hours}
              </div>
              {orphanage.open_on_weekends ? (
                <div className="open-on-weekends">
                  <FiInfo size={32} color="#39CC83" />
                  Atendemos <br />
                  fim de semana
                </div>
              ) : (
                <div className="open-on-weekends dont-open">
                  <FiInfo size={32} color="#FF669D" />
                  Não Atendemos em <br />
                  final de semana
                </div>
              )}
            </div>

            <a
              href={`https://api.whatsapp.com/send?phone=55${orphanage.phone}&text=ola%20vi%20seu%20post%20na%20happy`}
              className="contact-button"
            >
              <FaWhatsapp size={20} color="#FFF" />
              Entrar em contato
            </a>
          </div>
        </div>
      </main>
    </div>
  );
}
